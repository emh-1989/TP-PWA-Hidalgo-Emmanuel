#VAriable
mi_nombre = "Emmanuel"

# Mostrar el tipo de dato de la variable
print("Tipo de dato de mi_nombre:", type(mi_nombre))

# Acceder a la primera y última letra usando índices
primera_letra = mi_nombre[0]
ultima_letra = mi_nombre[-1]
print("Primera letra:", primera_letra)
print("Última letra:", ultima_letra)

# Calcular la longitud de mi_nombre usando len()
longitud = len(mi_nombre)
print("Longitud de mi_nombre:", longitud)

# Imprimir el contenido de mi_nombre en mayúsculas y minúsculas
print("En mayúsculas:", mi_nombre.upper())
print("En minúsculas:", mi_nombre.lower())